    /// <summary>
    /// 根据设置自动生成试卷
    /// </summary>
    /// <param name="sender"></param>
    /// <param name="e"></param>
    protected void lbtn_Conirm_Click(object sender, EventArgs e)
    {
        DBHelper db = new DBHelper();
        #region 判断试卷名称是否存在存在返回 并提示用户
        string strSQL = "SELECT COUNT([PaperID]) FROM [Paper] where CourseID =@CourseID and PaperName=@PaperName;";
        SqlParameter[] Params = new SqlParameter[2];
        Params[0] = db.MakeInParam("@CourseID", SqlDbType.Int, 4,Convert.ToInt32(ddlCourse.SelectedValue));
        Params[1] = db.MakeInParam("@PaperName", SqlDbType.VarChar, 200, txtPaperName.Text);
        if (db.ExecuteSelect(strSQL, Params) >= 1)
        {
            this.RequiredFieldValidator3.ErrorMessage = "已存在该试卷！";
            this.RequiredFieldValidator3.IsValid = false;
            this.txtPaperName.Focus();
            return;
        }
        else
        {
            this.RequiredFieldValidator3.ErrorMessage = "不能为空！";
            this.RequiredFieldValidator3.IsValid = true;
        }
        #endregion
        Panel1.Visible = true;
       
        string GridView1Str = "select top " + int.Parse(txtSingleNum.Text.Trim()) + " * from SingleProblem order by newid()";//根据参数设置查询单选题Sql语句
        DataSet ds1 = db.GetDataSet(GridView1Str);//调用DataBase类方法GetDataSetSql方法查询数据
        GridView1.DataSource = ds1.Tables[0].DefaultView;//为单选题GridView控件指名数据源
        GridView1.DataBind();//绑定数据
        string GridView2Str = "select top " + int.Parse(txtMultiNum.Text.Trim()) + " * from MultiProblem order by newid()";//根据参数设置查询多选题Sql语句
        DataSet ds2 = db.GetDataSet(GridView2Str);//调用DataBase类方法GetDataSetSql方法查询数据
        GridView2.DataSource = ds2.Tables[0].DefaultView;//为多选题GridView控件指名数据源
        GridView2.DataBind();//绑定数据
        string GridView3Str = "select top " + int.Parse(txtJudgeNum.Text.Trim()) + " * from JudgeProblem order by newid()";//根据参数设置查询判断题Sql语句
        DataSet ds3 = db.GetDataSet(GridView3Str);//调用DataBase类方法GetDataSetSql方法查询数据
        GridView3.DataSource = ds3.Tables[0].DefaultView;//为判断题GridView控件指名数据源
        GridView3.DataBind();//绑定数据
        string GridView4Str = "select top " + int.Parse(txtFillNum.Text.Trim()) + " * from FillBlankProblem order by newid()";//根据参数设置查询填空题Sql语句
        DataSet ds4 = db.GetDataSet(GridView4Str);//调用DataBase类方法GetDataSetSql方法查询数据
        GridView4.DataSource = ds4.Tables[0].DefaultView;//为填空题GridView控件指名数据源
        GridView4.DataBind();//绑定数据
        string GridView5Str = "select top " + int.Parse(txtQuestionNum.Text.Trim()) + " * from QuestionProblem order by newid()";//根据参数设置查询填空题Sql语句
